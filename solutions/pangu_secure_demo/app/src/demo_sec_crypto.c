void sec_crypto_rsa_1024_demo()
{
        sc_rsa_t         rsa;
        sc_rsa_context_t rsa_ctx;
        uint8_t          ci[128];
        uint8_t          pt[128];
        uint32_t         len;
        uint32_t         ret;
        uint8_t          sig[128];
        uint8_t          hash[20] = {0x6d, 0x97, 0xfe, 0x93, 0x52, 0x17, 0x7a,
                            0xb9, 0x1e, 0x9f, 0x1b, 0xf7, 0x9f, 0x8f,
                            0xd8, 0x6b, 0xdd, 0x6b, 0x4b, 0x75};

        ret = sc_rsa_init(&rsa, 0, SC_RSA_KEY_BITS_1024);
        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);

        /* NOTE TEE MUST using g_keybuf,  layout :n | e | d */
        memcpy(g_keybuf, g_n, 128);
        memcpy(g_keybuf + 128, g_e, 128);
        memcpy(g_keybuf + 256, g_d, 128);

        rsa_ctx.padding_type = SC_RSA_PADDING_MODE_PKCS1;
        rsa_ctx.n            = g_keybuf;
        rsa_ctx.e            = g_keybuf + 128;
        rsa_ctx.d            = g_keybuf + 256;
        rsa_ctx.hash_type    = SC_RSA_HASH_TYPE_SHA1;

        ret = sc_rsa_encrypt(&rsa, &rsa_ctx, g_pt, sizeof(g_pt), ci);
        TRACE();
        // printf("===%s, %d, %x\n", __FUNCTION__, __LINE__, 0);
        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);
        ret = sc_rsa_decrypt(&rsa, &rsa_ctx, ci, sizeof(ci), pt, &len);
        // printf("===%s, %d, %x\n", __FUNCTION__, __LINE__, 0);
        TRACE();
        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);
        CHECK_RET_WITHOUT_RET_P(len == sizeof(g_pt), len);
        ret = memcmp(pt, g_pt, len);
        CHECK_RESULT(ret == 0);
        aos_log_hexdump("sig", (char *)sig, 32);
        aos_log_hexdump("hash", (char *)hash, 20);
        ret =
            sc_rsa_sign(&rsa, &rsa_ctx, hash, 20, sig, SC_RSA_HASH_TYPE_SHA1);
        TRACE();
        // printf("===%s, %d, %x\n", __FUNCTION__, __LINE__, 0);
        aos_log_hexdump("sig", (char *)sig, 32);
        CHECK_RESULT(ret == SC_OK);
        ret = sc_rsa_verify(&rsa, &rsa_ctx, hash, 20, sig, 128,
                            SC_RSA_HASH_TYPE_SHA1);
        TRACE();
        printf("===%s, %d, %x\n", __FUNCTION__, __LINE__, ret);
        CHECK_RESULT(ret == true);

        sc_rsa_uninit(&rsa);
}

