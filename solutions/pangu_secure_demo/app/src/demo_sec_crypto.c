void sec_crypto_sha224_demo()
{
        uint32_t            out_size;
        uint32_t            ret;
        sc_sha_t            sc_sha;
        sc_sha_context_t    sc_ctx;
        const unsigned char input[56] = {
            "abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq"};
        uint8_t output[28];
        uint8_t sum_result[] = {0x75, 0x38, 0x8b, 0x16, 0x51, 0x27, 0x76,
                                0xcc, 0x5d, 0xba, 0x5d, 0xa1, 0xfd, 0x89,
                                0x01, 0x50, 0xb0, 0xc6, 0x45, 0x5c, 0xb4,
                                0xf5, 0x8b, 0x19, 0x52, 0x52, 0x25, 0x25};

        sc_sha_init(&sc_sha, 0);
        sc_sha_start(&sc_sha, &sc_ctx, SC_SHA_MODE_224);
        sc_sha_update(&sc_sha, &sc_ctx, input, 56);
        sc_sha_finish(&sc_sha, &sc_ctx, output, &out_size);
        ret = memcmp(output, sum_result, 28);
        CHECK_RESULT(ret == 0);
}

