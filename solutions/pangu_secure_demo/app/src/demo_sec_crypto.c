void sec_crypto_rsa_demo_2048_tee()
{
        sc_rsa_t         rsa;
        sc_rsa_context_t rsa_ctx;
        uint32_t         ret;

        ret = sc_rsa_init(&rsa, 0, SC_RSA_KEY_BITS_2048);
        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);

        /* NOTE TEE MUST using g_keybuf,  layout :n | e | d */
        memcpy(g_keybuf, test_nist_2048_n, 256);
        memcpy(g_keybuf + 256, test_nist_2048_e, 256);

        rsa_ctx.padding_type = SC_RSA_PADDING_MODE_PKCS1;
        rsa_ctx.n            = g_keybuf;
        rsa_ctx.e            = g_keybuf + 256;
        rsa_ctx.d            = g_keybuf + 512;
        rsa_ctx.hash_type    = SC_RSA_HASH_TYPE_SHA256;

        // Log_HexDump("sig", (char *)sig, 32);
        ret = sc_rsa_verify(&rsa, &rsa_ctx, test_digest, 32, sig2048_ex, 256,
                            SC_RSA_HASH_TYPE_SHA256);
        if (ret) {
                LOGE(TAG, "%s, %d, %x\n", __FUNCTION__, __LINE__, ret);
        } else {
                printf("rsa2048 passed!%s, %d\n", __FUNCTION__, __LINE__);
        }
        CHECK_RESULT(ret == true);

        sc_rsa_uninit(&rsa);
}

