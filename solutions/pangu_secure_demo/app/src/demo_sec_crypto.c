        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);
        ret =
            sc_aes_set_decrypt_key(&aes, (void *)key, SC_AES_KEY_LEN_BITS_128);
        CHECK_RET_WITHOUT_RET_P(ret == SC_OK, ret);
        //aos_log_hexdump("pt_c", (char *)pt_c, 32);
        //aos_log_hexdump("iv", (char *)iv, 16);
